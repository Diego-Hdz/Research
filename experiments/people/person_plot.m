function [] = person_plot(red, grey, key)
%% Creates a plot of the person experiment
%   param red: the red points for the graph
%   param grey: the grey points for the graph
%   param key: the reference key of the graph
p1_col = zeros(1,33)+1;
p2_col = zeros(1,33)+2;
p3_col = zeros(1,33)+3;
p4_col = zeros(1,33)+4;
p5_col = zeros(1,33)+5;
figure
%% Plot grey points
hold on
for i=1:length(grey)
    if i <= length(grey)/5
        plot(p1_col, grey(i), 'ko', 'MarkerSize' ,6, 'MarkerFaceColor', [0.5, 0.5, 0.5], 'LineWidth', 1);
    elseif (length(grey)/5 < i && i <= length(grey)*2/5)
        plot(p2_col, grey(i), 'ko', 'MarkerSize', 6, 'MarkerFaceColor', [0.5, 0.5, 0.5], 'LineWidth', 1);
    elseif (length(grey)*2/5 < i && i <= length(grey)*3/5)
        plot(p3_col, grey(i), 'ko', 'MarkerSize', 6, 'MarkerFaceColor', [0.5, 0.5, 0.5], 'LineWidth', 1);
    elseif (length(grey)*3/5 < i && i <= length(grey)*4/5)
        plot(p4_col, grey(i), 'ko', 'MarkerSize', 6, 'MarkerFaceColor', [0.5, 0.5, 0.5], 'LineWidth', 1);
    elseif i > length(grey)*4/5
        plot(p5_col, grey(i), 'ko', 'MarkerSize', 6, 'MarkerFaceColor', [0.5, 0.5, 0.5], 'LineWidth', 1);
    else
        error("Grey values for loop plotting error");
    end
end

%% Plot red points
hold on
for i=1:5
    if i == 1
        plot(p1_col,red(i), 'ko', 'MarkerSize', 7, 'MarkerFaceColor', [1, 0, 0], 'DisplayName', 'P1', 'LineWidth', 1);
        plot(p2_col,red(i+5), 'ko', 'MarkerSize', 7, 'MarkerFaceColor', [1, 0, 0], 'DisplayName', 'P2', 'LineWidth', 1);
        plot(p3_col,red(i+10), 'ko', 'MarkerSize', 7, 'MarkerFaceColor', [1, 0, 0], 'DisplayName', 'P3', 'LineWidth', 1);
        plot(p4_col,red(i+15), 'ko', 'MarkerSize', 7, 'MarkerFaceColor', [1, 0, 0], 'DisplayName', 'P4', 'LineWidth', 1);
        plot(p5_col,red(i+20), 'ko', 'MarkerSize', 7, 'MarkerFaceColor', [1, 0, 0], 'DisplayName', 'P5', 'LineWidth', 1);
    elseif i == 2
        plot(p1_col,red(i), 'ks', 'MarkerSize',7, 'MarkerFaceColor', [1, 0, 0], 'DisplayName', 'P1', 'LineWidth', 1);
        plot(p2_col,red(i+5), 'ks', 'MarkerSize', 7, 'MarkerFaceColor', [1, 0, 0], 'DisplayName', 'P2', 'LineWidth', 1);
        plot(p3_col,red(i+10), 'ks', 'MarkerSize', 7, 'MarkerFaceColor', [1, 0, 0], 'DisplayName', 'P3', 'LineWidth', 1);
        plot(p4_col,red(i+15), 'ks', 'MarkerSize', 7, 'MarkerFaceColor', [1, 0, 0], 'DisplayName', 'P4', 'LineWidth', 1);
        plot(p5_col,red(i+20), 'ks', 'MarkerSize', 7, 'MarkerFaceColor', [1, 0, 0], 'DisplayName', 'P5', 'LineWidth', 1);    
    elseif i == 3
        plot(p1_col,red(i), 'kd', 'MarkerSize',7, 'MarkerFaceColor', [1, 0, 0], 'DisplayName', 'P1', 'LineWidth', 1);
        plot(p2_col,red(i+5), 'kd', 'MarkerSize', 7, 'MarkerFaceColor', [1, 0, 0], 'DisplayName', 'P2', 'LineWidth', 1);
        plot(p3_col,red(i+10), 'kd', 'MarkerSize', 7, 'MarkerFaceColor', [1, 0, 0], 'DisplayName', 'P3', 'LineWidth', 1);
        plot(p4_col,red(i+15), 'kd', 'MarkerSize', 7, 'MarkerFaceColor', [1, 0, 0], 'DisplayName', 'P4', 'LineWidth', 1);
        plot(p5_col,red(i+20), 'kd', 'MarkerSize', 7, 'MarkerFaceColor', [1, 0, 0], 'DisplayName', 'P5', 'LineWidth', 1);
    elseif i == 4
        plot(p1_col,red(i), 'kh', 'MarkerSize',7, 'MarkerFaceColor', [1, 0, 0], 'DisplayName', 'P1', 'LineWidth', 1);
        plot(p2_col,red(i+5), 'kh', 'MarkerSize', 7, 'MarkerFaceColor', [1, 0, 0], 'DisplayName', 'P2', 'LineWidth', 1);
        plot(p3_col,red(i+10), 'kh', 'MarkerSize', 7, 'MarkerFaceColor', [1, 0, 0], 'DisplayName', 'P3', 'LineWidth', 1);
        plot(p4_col,red(i+15), 'kh', 'MarkerSize', 7, 'MarkerFaceColor', [1, 0, 0], 'DisplayName', 'P4', 'LineWidth', 1);
        plot(p5_col,red(i+20), 'kh', 'MarkerSize', 7, 'MarkerFaceColor', [1, 0, 0], 'DisplayName', 'P5', 'LineWidth', 1);
    else
        plot(p1_col,red(i), 'k^', 'MarkerSize', 7, 'MarkerFaceColor', [1, 0, 0], 'DisplayName', 'P1', 'LineWidth', 1);
        plot(p2_col,red(i+5), 'k^', 'MarkerSize', 7, 'MarkerFaceColor', [1, 0, 0], 'DisplayName', 'P2', 'LineWidth', 1);
        plot(p3_col,red(i+10), 'k^', 'MarkerSize', 7, 'MarkerFaceColor', [1, 0, 0], 'DisplayName', 'P3', 'LineWidth', 1);
        plot(p4_col,red(i+15), 'k^', 'MarkerSize', 7, 'MarkerFaceColor', [1, 0, 0], 'DisplayName', 'P4', 'LineWidth', 1);
        plot(p5_col,red(i+20), 'k^', 'MarkerSize', 7, 'MarkerFaceColor', [1, 0, 0], 'DisplayName', 'P5', 'LineWidth', 1);
    end
end

%% Plot information
title({"Euclidean distance of samples generated by different people",...
    "using the " + key + " key as the reference key"}, 'FontSize', 14);
xlabel("People", 'FontSize', 14)
ylabel("Euclidean Distance", 'FontSize', 14)
set(gca, 'XLim',[0.5, 5.5]);
set(gca, 'XTick',(1:1.0:5));
set(gca, 'XTickLabel', {'Person 1', 'Person 2', 'Person 3', 'Person 4', 'Person 5'});
h = zeros(6, 1);
h(1) = plot(NaN,NaN, 'ko', 'MarkerSize' ,6, 'MarkerFaceColor', [0.5, 0.5, 0.5], 'LineWidth', 1);
h(2) = plot(NaN,NaN, 'ko', 'MarkerSize', 7, 'MarkerFaceColor', [1, 0, 0], 'LineWidth', 1);
h(3) = plot(NaN,NaN, 'ks', 'MarkerSize', 7, 'MarkerFaceColor', [1, 0, 0], 'LineWidth', 1);
h(4) = plot(NaN,NaN, 'kd', 'MarkerSize', 7, 'MarkerFaceColor', [1, 0, 0], 'LineWidth', 1);
h(5) = plot(NaN,NaN, 'kh', 'MarkerSize', 7, 'MarkerFaceColor', [1, 0, 0], 'LineWidth', 1);
h(6) = plot(NaN,NaN, 'k^', 'MarkerSize', 7, 'MarkerFaceColor', [1, 0, 0], 'LineWidth', 1);
legend(h, 'Others', 'Person 1', 'Person 2', 'Person 3', 'Person 4', 'Person 5')

%% Save
savefig(sprintf("experiments/people/figures_p/People Distances_%d", key));
%savas(gcf, sprintf("experiments/people/figures_p/People Distances_%d.jpg", key))
close all;
end